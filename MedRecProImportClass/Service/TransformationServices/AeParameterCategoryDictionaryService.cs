        #region IAeParameterCategoryDictionaryService Implementation

        /**************************************************************/
        /// <inheritdoc />
        public string? Resolve(string? parameterName)
        {
            #region implementation

            if (string.IsNullOrWhiteSpace(parameterName))
                return null;

            return _parameterNameToSoc.TryGetValue(parameterName.Trim(), out var soc)
                ? soc
                : null;

            #endregion
        }

        /**************************************************************/
        /// <inheritdoc />
        public bool TryResolveObservation(ParsedObservation obs)
        {
            #region implementation

            // Guard: only ADVERSE_EVENT
            if (!string.Equals(obs.TableCategory, "ADVERSE_EVENT", StringComparison.OrdinalIgnoreCase))
                return false;

            // Guard: only NULL/whitespace ParameterCategory (never overwrite)
            if (!string.IsNullOrWhiteSpace(obs.ParameterCategory))
                return false;

            var resolved = Resolve(obs.ParameterName);
            if (resolved == null)
                return false;

            obs.ParameterCategory = resolved;
            obs.ValidationFlags = string.IsNullOrEmpty(obs.ValidationFlags)
                ? "DICT:SOC_RESOLVED"
                : $"{obs.ValidationFlags}; DICT:SOC_RESOLVED";

            return true;

            #endregion
        }

        /**************************************************************/
        /// <inheritdoc />
        public int Count => _parameterNameToSoc.Count;

        #endregion IAeParameterCategoryDictionaryService Implementation
